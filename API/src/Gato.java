
public class Gato {

	private String id, url; 
	private Integer width, height;
	
	public Gato(String id, String url, Integer width, Integer height) {
		super();
		this.id = id;
		this.url = url;
		this.width = width;
		this.height = height;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public Integer getWidth() {
		return width;
	}
	public void setWidth(Integer width) {
		this.width = width;
	}
	public Integer getHeight() {
		return height;
	}
	public void setHeight(Integer height) {
		this.height = height;
	}
	
	
	
	
	
	
	
}
